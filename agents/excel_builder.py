def _build_is(wb: xlsxwriter.Workbook, sty: StyleBook,
              statements: List[IncomeStatement], years: List[int]):
    ws = wb.add_worksheet(SH_IS)
    ws.freeze_panes(2, 1)
    ws.hide_gridlines(2)
    ws.set_column(0, 0, 32)   # Labels

    n = len(years)
    data_cols = _data_cols(years)  # [1, 2, 3] → cols B, C, D

    # Year columns
    for i, yr in enumerate(years):
        ws.set_column(data_cols[i], data_cols[i], 14)
        ws.write(0, data_cols[i], str(yr), sty.col_header)

    # YoY and CAGR columns
    yoy_start = data_cols[-1] + 1
    for i in range(n - 1):
        c = yoy_start + i
        ws.set_column(c, c, 12)
        if i + 1 < n:
            ws.write(0, c, f"YoY {years[i]}→{years[i+1]}", sty.col_header_gold)
    cagr_col = yoy_start + max(n - 1, 0)
    if n > 1:
        ws.set_column(cagr_col, cagr_col, 12)
        ws.write(0, cagr_col, f"CAGR {years[0]}→{years[-1]}", sty.col_header_gold)

    def lbl(row: int, text: str, fmt=None):
        ws.write(row - 1, 0, text, fmt or sty.line_label)

    def val(row: int, col_idx: int, value, fmt=None):
        _write_val(ws, row - 1, col_idx, value, fmt or sty.num)

    def fml(row: int, col_idx: int, formula: str, fmt=None):
        ws.write_formula(row - 1, col_idx, formula, fmt or sty.num)

    def xc(col_idx: int) -> str:
        return col_letter(col_idx + 1)  # 0-based col → Excel letter

    # ── Labels ────────────────────────────────────────────────────────────────
    ws.write(IS["header"] - 1, 0, "INCOME STATEMENT", sty.col_header)

    lbl(IS["revenue"], "Net Revenue / Net Sales")
    lbl(IS["other_income"], "Other Income")
    lbl(IS["total_income"], "Total Income", sty.subtotal_label)

    lbl(IS["cost_hdr"], "COST STRUCTURE", sty.section_label)
    lbl(IS["cogs"], "Cost of Revenue / COGS")
    lbl(IS["employee"], "Employee Expenses")
    lbl(IS["other_opex"], "Other Operating Expenses")
    lbl(IS["total_opex"], "Total Operating Expenses", sty.subtotal_label)

    lbl(IS["prof_hdr"], "PROFITABILITY", sty.section_label)
    lbl(IS["gross_profit"], "Gross Profit", sty.subtotal_label)
    lbl(IS["gross_margin"], "  Gross Margin %")
    lbl(IS["ebitda"], "EBITDA", sty.subtotal_label)
    lbl(IS["ebitda_margin"], "  EBITDA Margin %")
    lbl(IS["da"], "Depreciation & Amortisation")
    lbl(IS["ebit"], "EBIT (Operating Profit)", sty.subtotal_label)
    lbl(IS["ebit_margin"], "  EBIT Margin %")
    lbl(IS["interest"], "Interest / Finance Costs")
    lbl(IS["pbt"], "Profit Before Tax (PBT)", sty.subtotal_label)
    lbl(IS["tax"], "Tax Expense")
    lbl(IS["eff_tax"], "  Effective Tax Rate %")
    lbl(IS["pat"], "NET PROFIT (PAT)", sty.subtotal_label)
    lbl(IS["net_margin"], "  Net Margin %")

    lbl(IS["pershare_hdr"], "PER SHARE DATA", sty.section_label)
    lbl(IS["shares"], "Shares Outstanding (Mn)")
    lbl(IS["eps"], "EPS (Basic)", sty.subtotal_label)
    lbl(IS["dps"], "Dividends Per Share")
    lbl(IS["payout"], "  Payout Ratio %")

    # ── Values and Formulas per Year ──────────────────────────────────────────
    for i, (yr, stmt) in enumerate(zip(years, statements)):
        c = data_cols[i]
        cx = xc(c)

        # Raw values (extracted)
        val(IS["revenue"], c, stmt.revenue)
        val(IS["other_income"], c, stmt.other_income)
        val(IS["cogs"], c, stmt.cogs)
        val(IS["employee"], c, stmt.employee_expenses)
        val(IS["other_opex"], c, stmt.other_operating_expenses)
        val(IS["da"], c, stmt.depreciation_amortization)
        val(IS["interest"], c, stmt.interest_expense)
        val(IS["tax"], c, stmt.tax_expense)
        val(IS["shares"], c, stmt.shares_outstanding, sty.num_int)
        val(IS["dps"], c, stmt.dividends_per_share)

        # EBITDA — use extracted if available, else compute
        if stmt.ebitda is not None:
            val(IS["ebitda"], c, stmt.ebitda, sty.num_sub)
        elif stmt.revenue is not None and stmt.cogs is not None:
            # approximate: revenue - opex components (no DA yet)
            val(IS["ebitda"], c, stmt.ebitda, sty.num_sub)

        # PAT — use extracted if available
        if stmt.pat is not None:
            val(IS["pat"], c, stmt.pat, sty.num_sub)

        # PBT — use extracted if available
        if stmt.pbt is not None:
            val(IS["pbt"], c, stmt.pbt, sty.num_sub)

        # Computed formulas
        fml(IS["total_income"], c, total_income(cx, cx, IS["other_income"])
            .replace(f"{cx}{IS['other_income']}", f"{cx}{IS['other_income']}"), sty.num_sub)

        fml(IS["gross_profit"], c,
            gross_profit(IS["revenue"], IS["cogs"], cx), sty.num_sub)
        fml(IS["gross_margin"], c,
            gross_margin(IS["gross_profit"], IS["revenue"], cx), sty.pct)
        fml(IS["ebitda_margin"], c,
            ebitda_margin(IS["ebitda"], IS["revenue"], cx), sty.pct)
        fml(IS["ebit"], c,
            ebit(IS["ebitda"], IS["da"], cx), sty.num_sub)
        fml(IS["ebit_margin"], c,
            ebit_margin(IS["ebit"], IS["revenue"], cx), sty.pct)
        fml(IS["eff_tax"], c,
            effective_tax_rate(IS["tax"], IS["pbt"], cx), sty.pct)
        fml(IS["net_margin"], c,
            net_margin(IS["pat"], IS["revenue"], cx), sty.pct)
        fml(IS["eps"], c,
            eps(IS["pat"], IS["shares"], cx), sty.num_sub)
        fml(IS["payout"], c,
            payout_ratio(IS["dps"], IS["eps"], cx), sty.pct)

    # ── YoY Growth Columns ────────────────────────────────────────────────────
    growth_rows = [IS["revenue"], IS["ebitda"], IS["ebit"], IS["pat"]]
    for i in range(n - 1):
        yc = yoy_start + i
        prev_cx = xc(data_cols[i])
        curr_cx = xc(data_cols[i + 1])
        for r in growth_rows:
            fml(r, yc, yoy_growth(curr_cx, prev_cx, r), sty.pct_growth_pos)

    # ── CAGR Column ───────────────────────────────────────────────────────────
    if n > 1:
        start_cx = xc(data_cols[0])
        end_cx = xc(data_cols[-1])
        periods = n - 1
        for r in growth_rows:
            fml(r, cagr_col, cagr(end_cx, start_cx, r, periods), sty.pct_growth_pos)

    return ws


