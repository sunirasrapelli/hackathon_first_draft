def _build_verification(wb: xlsxwriter.Workbook, sty: StyleBook, years: List[int]):
    ws = wb.add_worksheet(SH_VR)
    ws.hide_gridlines(2)
    ws.set_column(0, 0, 40)

    data_cols = _data_cols(years)
    for i, yr in enumerate(years):
        ws.set_column(data_cols[i], data_cols[i], 16)
        ws.write(0, data_cols[i], str(yr), sty.col_header)

    ws.write(0, 0, "VERIFICATION & QUALITY CHECKS", sty.col_header)

    sections = [
        # (row, label, formula_template)
        (2, "1. BALANCE SHEET EQUATION", None),
        (3, "   Total Assets", f"='{SH_BS}'!{{c}}{BS['total_assets']}"),
        (4, "   Total Liabilities + Equity", f"='{SH_BS}'!{{c}}{BS['total_le']}"),
        (5, "   Difference", f"=B3-B4"),
        (6, "   STATUS",
         f'=IF(ABS({{c}}3-{{c}}4)<1,"✓ PASS","✗ FAIL")'),
        (8, "2. CASH FLOW RECONCILIATION", None),
        (9, "   Net Change in Cash (CF Statement)", f"='{SH_CF}'!{{c}}{CF['net_change']}"),
        (10, "  Closing - Opening (Balance Sheet)", f"='{SH_CF}'!{{c}}{CF['close_cash']}-'{SH_CF}'!{{c}}{CF['open_cash']}"),
        (11, "  Difference", "={c}9-{c}10"),
        (12, "  STATUS",
         '=IF(ABS({c}9-{c}10)<1,"✓ PASS","✗ FAIL")'),
        (14, "3. PAT CONSISTENCY (IS vs CF)", None),
        (15, "   PAT (Income Statement)", f"='{SH_IS}'!{{c}}{IS['pat']}"),
        (16, "   Net Income (Cash Flow)", f"='{SH_CF}'!{{c}}{CF['net_income']}"),
        (17, "   Difference", "={c}15-{c}16"),
        (18, "   STATUS",
         '=IF(ABS({c}15-{c}16)/{c}15<0.05,"✓ PASS","⚠ WARNING >5%")'),
    ]

    for row, label, _ in sections:
        ws.write(row - 1, 0, label,
                 sty.section_label if label[0].isdigit() else sty.line_label)

    for i, c in enumerate(data_cols):
        cx = col_letter(c + 1)

        # BS check
        ws.write_formula(2, c, f"='{SH_BS}'!{cx}{BS['total_assets']}", sty.num)
        ws.write_formula(3, c, f"='{SH_BS}'!{cx}{BS['total_le']}", sty.num)
        ws.write_formula(4, c, f"={cx}3-{cx}4", sty.num)
        ws.write_formula(5, c,
            f'=IF(ABS({cx}3-{cx}4)<1,"✓ PASS","✗ FAIL")',
            sty.pass_fmt)

        # CF reconciliation
        ws.write_formula(8, c, f"='{SH_CF}'!{cx}{CF['net_change']}", sty.num)
        ws.write_formula(9, c,
            f"='{SH_CF}'!{cx}{CF['close_cash']}-'{SH_CF}'!{cx}{CF['open_cash']}",
            sty.num)
        ws.write_formula(10, c, f"={cx}9-{cx}10", sty.num)
        ws.write_formula(11, c,
            f'=IF(ABS({cx}9-{cx}10)<1,"✓ PASS","✗ FAIL")',
            sty.pass_fmt)

        # PAT consistency
        ws.write_formula(14, c, f"='{SH_IS}'!{cx}{IS['pat']}", sty.num)
        ws.write_formula(15, c, f"='{SH_CF}'!{cx}{CF['net_income']}", sty.num)
        ws.write_formula(16, c, f"={cx}15-{cx}16", sty.num)
        ws.write_formula(17, c,
            f'=IF(IFERROR(ABS({cx}15-{cx}16)/ABS({cx}15),1)<0.05,'
            f'"✓ PASS","⚠ WARNING >5%")',
            sty.warn_fmt)

    return ws


