def _write_val(ws, row: int, col: int, value, fmt=None):
    """Write a number or None (skip None to leave cell empty)."""
    if value is None:
        return
    if fmt:
        ws.write_number(row, col, value, fmt)
    else:
        ws.write_number(row, col, value)


def _data_cols(years: List[int]) -> List[int]:
    """Return 0-based column indices for each year's data (starting at col 1 = B)."""
    return [i + 1 for i in range(len(years))]


# ── Sheet: Cover ──────────────────────────────────────────────────────────────

def _build_cover(wb: xlsxwriter.Workbook, sty: StyleBook,
                 company: str, years: List[int]):
    ws = wb.add_worksheet("Cover")
    ws.hide_gridlines(2)
    ws.set_column(0, 7, 14)
    ws.set_row(0, 20)
    ws.set_row(2, 50)
    ws.set_row(4, 30)
    ws.set_row(6, 25)

    ws.merge_range("A3:H3", company, sty.cover_title)
    ws.merge_range("A4:H4", "Financial Analysis Report", sty.cover_sub)
    ws.merge_range("A5:H5",
                   f"Fiscal Years: {' | '.join(str(y) for y in sorted(years))}",
                   sty.cover_body)

    # Table of Contents
    ws.write("A8", "Contents", sty.subtitle)
    sheets = [
        (SH_IS,  "Income Statement (Profit & Loss)"),
        (SH_BS,  "Balance Sheet"),
        (SH_CF,  "Cash Flow Statement"),
        (SH_RT,  "Financial Ratios & Analysis"),
        (SH_VR,  "Verification & Quality Checks"),
        (SH_ST,  "Settings & Assumptions"),
        (SH_WC,  "WACC & Cost of Capital"),
        (SH_DCF, "DCF Valuation (5-Year FCF Model)"),
        (SH_DDM, "DDM Valuation (Dividend Discount)"),
        (SH_CMP, "Comparable Company Analysis"),
        ("Charts","Charts & Visualisations"),
    ]
    for i, (sheet, label) in enumerate(sheets):
        ws.write_url(f"A{9+i}", f"internal:'{sheet}'!A1", sty.cover_link, label)

    # Footer
    ts = datetime.now().strftime("%B %d, %Y %H:%M")
    ws.merge_range(f"A{16}:H{16}",
                   f"Generated by Financial Analysis AI  ·  {ts}",
                   sty.cover_footer)
    return ws


