# ── Public Entry Point ────────────────────────────────────────────────────────

def build_workbook(financial_data: FinancialData, output_path: Optional[str] = None) -> str:
    """
    Build the full Excel workbook from FinancialData.
    Returns the path to the generated .xlsx file.
    """
    years = financial_data.sorted_years()
    if not years:
        raise ValueError("FinancialData has no fiscal_years — cannot build workbook.")

    if output_path is None:
        safe_name = financial_data.company_name.replace(" ", "_").replace("/", "-")
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = str(EXCEL_DIR / f"{safe_name}_{ts}.xlsx")

    global data_cols, _last_data_col
    data_cols = _data_cols(years)
    _last_data_col = data_cols[-1]

    log.info(f"Building Excel workbook → {output_path}")

    wb = xlsxwriter.Workbook(output_path)
    sty = StyleBook(wb)

    # Align statements to years (insert None-filled stubs for missing years)
    def align(stmts, cls):
        by_year = {s.fiscal_year: s for s in stmts}
        return [by_year.get(yr, cls(fiscal_year=yr)) for yr in years]

    income_stmts = align(financial_data.income_statements, IncomeStatement)
    balance_sheets = align(financial_data.balance_sheets, BalanceSheet)
    cash_flows = align(financial_data.cash_flow_statements, CashFlowStatement)

    from agents.valuation_builder import (
        build_comps_sheet,
        build_dcf_sheet,
        build_ddm_sheet,
        build_wacc_sheet,
    )
    from agents.charts_builder import build_charts_sheet

    _build_cover(wb, sty, financial_data.company_name, years)
    _build_is(wb, sty, income_stmts, years)
    _build_bs(wb, sty, balance_sheets, years)
    _build_cf(wb, sty, cash_flows, years)
    _build_ratios(wb, sty, years)
    _build_verification(wb, sty, years)
    _build_settings(wb, sty)
    build_wacc_sheet(wb, sty, years)
    build_dcf_sheet(wb, sty, years)
    build_ddm_sheet(wb, sty, years)
    build_comps_sheet(wb, sty, years)
    build_charts_sheet(wb, sty, years)

    wb.close()
    log.info(f"Workbook saved: {output_path}")
    return output_path
